import sys

def lookup(n):
    # Windows CE doesnt seem to provide an easy way to
    # get a meaningful string from an error code. This
    # function provides a low-overhead way to at least 
    # get the symbolic name of the error.

    dict=sys.modules['win32error'].__dict__
    for k, v in dict.items():
        if v==n and k[0]=='E': return k
    return 'ERROR_%s' % n


